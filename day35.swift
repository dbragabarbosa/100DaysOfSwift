---------- WRAP UP 

À medida que sua habilidade Swift aumenta, espero que você esteja começando a sentir que 
o equilíbrio desses projetos se afasta da explicação do básico para apresentar e dissecar código.

In this project you learned how to download JSON using Swift’s Data type, then use the Codable 
protocol to convert that data into Swift objects we defined. Working with JSON is something you're 
going to be doing time and time again in your Swift career, and you've cracked it in about an hour 
of work – while also learning about UITabBarController, UIStoryboard, and more.


---------- Desafio

1.Adicione um botão Créditos ao canto superior direito usando UIBarButtonItem. Quando isso for tocado, 
mostre um alerta informando aos usuários que os dados vêm da API We The People da Whitehouse.

2. Let users filter the petitions they see. This involves creating a second array of filtered items that 
contains only petitions matching a string the user entered. Use a UIAlertController with a text field to 
let them enter that string. This is a tough one, so I’ve included some hints below if you get stuck.

3. Experimente o HTML - este não é um tutorial HTML ou CSS, mas você pode encontrar muitos recursos 
on-line para lhe dar conhecimento suficiente para mexer um pouco com o layout.
