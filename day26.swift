Desafio

Uma das melhores maneiras de aprender é escrever seu próprio código o mais rápido possível, 
então aqui estão três maneiras pelas quais você deve tentar estender este aplicativo para garantir que 
você entenda completamente o que está acontecendo:

1. Se os usuários tentarem visitar uma URL que não é permitida, mostre um alerta dizendo que está bloqueada.

2. Try making two new toolbar items with the titles Back and Forward. You should make them use webView.goBack and webView.goForward.

3. Para mais um desafio, tente alterar o controlador de visualização inicial para uma visualização de tabela, 
como no projeto 1, onde os usuários podem escolher seu site a partir de uma lista, em vez de apenas ter o primeiro 
na matriz carregado antecipadamente.

Dica: Depois de concluir o projeto 5, você pode voltar aqui para adicionar a opção de carregar a lista de sites a 
partir de um arquivo, em vez de codificá-los em uma matriz.